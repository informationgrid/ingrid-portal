package de.ingrid.portal.om;


public class IngridFragmentPrefValue {

	private Long id;
	private Long prefId;
	private Long prefValueOrder;
	private String prefValue;
	
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getPrefId() {
		return prefId;
	}
	public void setPrefId(Long prefId) {
		this.prefId = prefId;
	}
	public Long getPrefValueOrder() {
		return prefValueOrder;
	}
	public void setPrefValueOrder(Long prefValueOrder) {
		this.prefValueOrder = prefValueOrder;
	}
	public String getPrefValue() {
		return prefValue;
	}
	public void setPrefValue(String prefValue) {
		this.prefValue = prefValue;
	}
			
}